$(document).ready(function() {
    $('#book_appointment').click(function(e) {
        e.preventDefault();

        var name = $('#client_name').val();
        var phone = $('#client_phone').val().trim();
        var day = $('#appointment_day').val().trim();
        var time = $('#appointment_time').val().trim();


        if (name.length == 0 || phone.length == 0 || day.length == 0 || time.length == 0) {

            swal({

                text: "Ooops! You forgot some Information!!",
                icon: "warning",
                width: '200px',
                timer: 3500
            });
            return false;

        } else {
            $.ajax({
                type: "POST",
                url: "appointment_submit.php",
                data: {
                    "name": name,
                    "phone": phone,
                    "day": day,
                    "time": time

                },
                cache: false,
                success: function(dataResult) {
                    var dataResult = JSON.parse(dataResult);

                    if (dataResult.statusCode == 200) {
                        $('#appointment_form')[0].reset();
                       // window.location = "//url/make_payment";

                        swal({
                            text: "Appointment Successfully Booked!!",
                            icon: "success",
                            width: '200px',
                            timer: 3500
                        });
                    } else if (dataResult.statusCode == 201) {
                        $('#appointment_form')[0].reset();
                       // window.location = "//url/make_payment";

                        swal({
                            text: "Appointment Already Made!!",
                            icon: "warning",
                            width: '200px',
                            timer: 3500
                        });

                    } else if (dataResult.statusCode == 202) {
                        //$('#appointment_form')[0].reset();

                        swal({
                            text: "Day fully Booked.Please Try another Day!!",
                            icon: "warning",
                            width: '200px',
                            timer: 3500
                        });

                    }

                }
            });
        }

    });
});


